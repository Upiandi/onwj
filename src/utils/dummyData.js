/**
 * Pagination helper
 */
export const getPaginatedData = (items, page = 1, perPage = 6) => {
  const startIndex = (page - 1) * perPage;
  const endIndex = startIndex + perPage;
  const data = items.slice(startIndex, endIndex);
  const total = items.length;
  const lastPage = Math.ceil(total / perPage);

  return {
    data,
    current_page: page,
    per_page: perPage,
    total,
    last_page: lastPage,
  };
};

/**
 * Filter berita by category and search
 */
export const filterBerita = (items, category, searchTerm) => {
  let filtered = items;

  if (category && category !== 'All') {
    filtered = filtered.filter(item => item.category === category);
  }

  if (searchTerm) {
    const term = searchTerm.toLowerCase();
    filtered = filtered.filter(
      item =>
        item.title.toLowerCase().includes(term) ||
        item.short_description.toLowerCase().includes(term)
    );
  }

  return filtered;
};

/**
 * Filter penghargaan by year
 */
export const filterPenghargaan = (items, year) => {
  if (year === 'all' || !year) {
    return items;
  }
  return items.filter(item => item.year === parseInt(year));
};

/**
 * Filter UMKM by category
 */
export const filterUmkm = (items, category) => {
  if (category === 'Semua') {
    return items;
  }
  return items.filter(item => item.category === category);
};

